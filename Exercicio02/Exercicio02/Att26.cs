using System;

namespace Exercicio02
{
    public class Att26
    {
        public static void Executar()
        {
            Console.WriteLine("Vamos gerar os números em ordem decrescente de 100 a 1:");

            Console.WriteLine("Números em ordem decrescente:");

            for (int i = 100; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();
            Console.Clear();
        }
    }
